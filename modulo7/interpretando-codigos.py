n = int(input())
m = 1

while m <= 10:
  print("{} x {} = {}".format(n, m, n*m))
  m += 1

# Explicação:
# A finalidade do código é agir como uma tabuada da multiplicação. 
# Para todo determinado valor n, o programa tem a função de mostrar o
# resultado das multiplicações desse número n pelos números de 1 à 10.
